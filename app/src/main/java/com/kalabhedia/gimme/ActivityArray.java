package com.kalabhedia.gimme;

public class ActivityArray {
    public String time;
    public String name;
    public String money;
    public String reason;
    public String code1;
    public String code2;

    public ActivityArray(String time, String name, String money, String reason, String code1, String code2) {
        this.time=time;
        this.name = name;
        this.money = money;
        this.reason=reason;
        this.code1 = code1;
        this.code2 = code2;
    }

}
